<h1 class="mt-4 mb-5">Help</h1>

<h2 class="mb-4">For Normal Users</h2>

    <h4 class="mt-5">How do I create an account?</h4>
    <p>To create an account, go to <a href="/register">the register page</a> and enter your information along with your school's join code.</p>

    <h4 class="mt-5">How do I login to my account?</h4>
    <p>To login to your account, go to <a href="/login">the login page</a> and enter your information.</p>

    <h4 class="mt-5">How do I redeem/scan a code?</h4>
    <p>To redeem a code, go to your <a href="/school/a">school's page</a> and scroll to the "Enter or Scan code section. From there you can either manually enter the code or you can scan the Qr code with your camera.</p>

    <h4 class="mt-5">How do I see my points?</h4>
    <p>To see your points, go to your <a href="/school/a">school's page</a> and scroll to the "Your Profile" section. There it shows your name, username, and points.</p>

<h2 class="m-5">For Admins</h2>

    <h4 class="mt-5">Where do I find my school's join code?</h4>
    <p>To get your school's join code, go to your <a href="/school/a">school's page</a> and the code is directly below your school's name.</p>

    <h4 class="mt-5">How do I get a report of all my students?</h4>
    <p>To get a report of your students, go to your <a href="/school/a">school's page</a> and then scroll to the bottom of the "Leader Board" section and click the button titled "Generate Report."</p>

    <h4 class="mt-5">How do I get random winners?</h4>
    <p>To get random winners, go to your <a href="/school/a">school's page</a> and then scroll to the bottom of the "Leader Board" section and click the button titled "Get Random Winners."</p>

    <h4 class="mt-5">How do I add/remove prizes?</h4>
    <p>To add and remove prizes, go to your <a href="/school/a">school's page</a> and then scoll to the "Prizes" section and click the edit (Pencile and square) and either delete prizes by clicking the trashcan icon, or add a new prize by entering the name, points, and an image.</p>

    <h4 class="mt-5">How do I redeem prizes?</h4>
    <p>To redeem prizes, go to your <a href="/school/a">school's page</a> and then click the student's name in the "Leader Board" section (Note: You may need to expand the leaderboard using the icon in the top right of the section) and then under the prize you wish to redeem, click "Redeem."</p>

    <h4 class="mt-5">How do I add/remove events?</h4>
    <p>To add and remove events, go to your <a href="/school/a">school's page</a> and then scoll to the "Events" section and click the edit (Pencile and square) and either delete events by clicking the trashcan icon, or add a new event by entering the name and points.</p>

    <h4 class="mt-5">How do I see the code for my event?</h4>
    <p>To get the code for your event, go to your <a href="/school/a">school's page</a> and then scroll to the "Events" section and click the "Show Codes" button and the codes will be visible.</p>

    <h4 class="mt-5">How do I get the Qr Code for my event?</h4>
    <p>To get the Qr Code for you event, go to your <a href="/school/a">school's page</a> and scroll to the "Events" section and click "Print Qr Code" under the event you wish to get the Qr Code for.</p>

